export function buildMockSearchResult(query, contract = sampleContracts[0]) {
  const topClause = contract.clauses.find((clause) => clause.riskLabel === 'high') || contract.clauses[0];

  return {
    query,
    matches: [
      {
        id: topClause.id,
        score: 0.91,
        metadata: {
          clauseType: topClause.clauseType,
          riskLabel: topClause.riskLabel,
          clauseText: topClause.clauseText,
        },
      },
      {
        id: contract.clauses[1]?.id || topClause.id,
        score: 0.84,
        metadata: {
          clauseType: contract.clauses[1]?.clauseType || topClause.clauseType,
          riskLabel: contract.clauses[1]?.riskLabel || topClause.riskLabel,
          clauseText: contract.clauses[1]?.clauseText || topClause.clauseText,
        },
      },
    ],
    reasoning: {
      answer: `The strongest match for "${query}" is the ${topClause.clauseType.replace(/_/g, ' ')} clause in ${contract.title}. It stands out because the language creates one-sided exposure without enough procedural safeguards.`,
      recommendations: [
        'Add a cure period and written notice before enforcement.',
        'Align the clause with governing law and dispute-resolution language.',
      ],
      supportingMatches: [
        {
          id: topClause.id,
          score: 0.91,
          clauseType: topClause.clauseType,
          riskLabel: topClause.riskLabel,
          clauseText: topClause.clauseText,
        },
      ],
    },
  };
}
